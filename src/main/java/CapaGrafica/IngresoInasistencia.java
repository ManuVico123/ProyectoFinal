    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        try {
        String fechaIn = txtFechaIn.getText();
        String fechaFin = txtFechaFin.getText();
        String materia = txtMateria.getText();
        String profesor = txtProfesor.getText();

        if (fechaIn.isEmpty() || fechaFin.isEmpty() || materia.isEmpty() || profesor.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Debe completar todos los campos.");
            return;
        }

       
        Inasistencia nueva = new Inasistencia();
        nueva.setFechaIn(fechaIn);
        nueva.setFechaFin(fechaFin);
        nueva.setMateria(materia);
        nueva.setProfesor(profesor);

        fachadaIn.RegistrarInasistencia(nueva);
        JOptionPane.showMessageDialog(null, "Inasistencia registrada con Ã©xito");
         InasistenciasDocentes Atras = new InasistenciasDocentes();
    Atras.setVisible(true);
    this.dispose();

    } catch (Exception ex) {
        JOptionPane.showMessageDialog(null, "Error al registrar: " + ex.getMessage());
    }
    }//GEN-LAST:event_btnIngresarActionPerformed

